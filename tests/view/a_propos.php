		function animateBarOnScroll() {
			var winScroll = document.body.scrollTop || document.documentElement.scrollTop;
			var height = document.documentElement.scrollHeight - document.documentElement.clientHeight;
			var scrolled = (winScroll / height) * 100;
			document.getElementById("progression").style.width = scrolled + "%";

			document.getElementById("leftBar").style.height = scrolled + "%";
			document.getElementById("rightBar").style.height = scrolled + "%";


			var y = $(this).scrollTop();

			if (y > 100) {
				$('#announce').fadeIn();
				$('#icon-bar').fadeIn();
			} else{
				$('#announce').fadeOut();
				$('#icon-bar').fadeOut();
			}


			if(scrolled >= 98) {
				$('#announce').css({
					'opacity':'0',
					'visibility':'hidden'
				});
			} else{
				$('#announce').css({
					'opacity':'1',
					'visibility':'visible'
				});
			}

		}

